class Dinosaur:
    X_POS = 90
    Y_POS = 330
    Y_POS_DUCK = 355
    JUMP_VEL = 17
    JUMP_GRAV = 1.1

    points = 0
    is_alive = True

    def __init__(self):
        self.duck_img = DUCKING
        self.run_img = RUNNING
        self.jump_img = JUMPING

        self.dino_duck = False
        self.dino_run = True
        self.dino_jump = False

        self.step_index = 0
        self.jump_vel = 0
        self.jump_grav = self.JUMP_VEL
        self.image = self.run_img[0]
        self.dino_rect = self.image.get_rect()
        self.dino_rect.x = self.X_POS
        self.dino_rect.y = self.Y_POS

    def update(self, userInput):
        if self.dino_duck and not self.dino_jump:
            self.duck()
        if self.dino_run:
            self.run()
        if self.dino_jump:
            self.jump()

        if self.step_index >= 20:
            self.step_index = 0

        if userInput == "K_UP" and not self.dino_jump:
            self.dino_duck = False
            self.dino_run = False
            self.dino_jump = True
        elif userInput == "K_DOWN" and not self.dino_jump:
            self.dino_duck = True
            self.dino_run = False
            self.dino_jump = False
        elif userInput == "K_DOWN":
            self.dino_duck = True
            self.dino_run = False
            self.dino_jump = True
        elif not (self.dino_jump or userInput == "K_DOWN"):
            self.dino_duck = False
            self.dino_run = True
            self.dino_jump = False

    def duck(self):
        self.image = self.duck_img[self.step_index // 10]
        self.dino_rect = self.image.get_rect()
        self.dino_rect.x = self.X_POS
        self.dino_rect.y = self.Y_POS_DUCK
        self.step_index += 1

    def run(self):
        self.image = self.run_img[self.step_index // 10]
        self.dino_rect = self.image.get_rect()
        self.dino_rect.x = self.X_POS
        self.dino_rect.y = self.Y_POS
        self.step_index += 1

    def jump(self):
        self.image = self.jump_img
        if self.dino_duck:
            self.jump_grav = self.JUMP_GRAV * 4
        if self.dino_jump:
            self.dino_rect.y -= self.jump_vel
            self.jump_vel -= self.jump_grav
        if self.dino_rect.y > self.Y_POS + 10:
            self.dino_jump = False
            self.jump_vel = self.JUMP_VEL
            self.jump_grav = self.JUMP_GRAV
            self.dino_rect.y = self.Y_POS

    def draw(self, SCREEN):
        SCREEN.blit(self.image, (self.dino_rect.x, self.dino_rect.y))

    def getXY(self):
        return (self.dino_rect.x, self.dino_rect.y)


class Cloud:
    def __init__(self):
        self.x = SCREEN_WIDTH + random.randint(800, 1000)
        self.y = random.randint(50, 100)
        self.image = CLOUD
        self.width = self.image.get_width()

    def update(self, game_speed):
        self.x -= game_speed
        if self.x < -self.width:
            self.x = SCREEN_WIDTH + random.randint(2500, 3000)
            self.y = random.randint(50, 100)

    def draw(self, SCREEN):
        SCREEN.blit(self.image, (self.x, self.y))


class Obstacle():
    def __init__(self, image, type):
        super().__init__()
        self.image = image
        self.type = type
        self.rect = self.image[self.type].get_rect()

        self.rect.x = SCREEN_WIDTH

    def update(self, game_speed):
        self.rect.x -= game_speed
        if self.rect.x < - self.rect.width:
            obstacles.pop(0)

    def draw(self, SCREEN):
        SCREEN.blit(self.image[self.type], self.rect)

    def getXY(self):
        return (self.rect.x, self.rect.y)

    def getLength(self):
        return self.image[self.type].get_width()

    def getHeight(self):
        return y_pos_bg - self.rect.y

    def getType(self):
        return (self.type)


class SmallCactus(Obstacle):
    def __init__(self, image):
        self.type = random.randint(0, 2)
        super().__init__(image, self.type)
        self.rect.y = 345


class LargeCactus(Obstacle):
    def __init__(self, image):
        self.type = random.randint(0, 2)
        super().__init__(image, self.type)
        self.rect.y = 325


class Bird(Obstacle):
    def __init__(self, image):
        self.type = 0
        super().__init__(image, self.type)

        # High, middle or ground
        if random.randint(0, 3) == 0:
            self.rect.y = 345
        elif random.randint(0, 2) == 0:
            self.rect.y = 260
        else:
            self.rect.y = 300
        self.index = 0

    def draw(self, SCREEN):
        if self.index >= 19:
            self.index = 0
        SCREEN.blit(self.image[self.index // 10], self.rect)
        self.index += 1

def playerKeySelector():
    userInputArray = pygame.key.get_pressed()

    if userInputArray[pygame.K_UP]:
        return "K_UP"
    elif userInputArray[pygame.K_DOWN]:
        return "K_DOWN"
    else:
        return "K_NO"

class NNInput():
    def __init__(self, distance_from_obstacle, obstacle_length, obstacle_height, dino_height, game_speed):
        self.distance_from_obstacle = distance_from_obstacle
        self.obstacle_length = obstacle_length
        self.obstacle_height = obstacle_height
        self.dino_height = dino_height
        self.game_speed = game_speed

    def to_tensor(self):
        return FloatTensor([self.distance_from_obstacle, self.obstacle_length, self.obstacle_height, self.dino_height, self.game_speed])

def playGame(players_and_states):
    dinos = []
    for p, s in players_and_states:
        dinos.append((Dinosaur(), (p, s)))

    num_dinos = len(dinos)

    input = NNInput(1500, 0, 0, 0, 10)

    global x_pos_bg, y_pos_bg, obstacles
    run = True
    clock = pygame.time.Clock()
    cloud = Cloud()
    x_pos_bg = 0
    y_pos_bg = 383
    font = pygame.font.Font('freesansbold.ttf', 20)
    obstacles = []
    spawn_dist = 0

    def score(points, current_game_speed):
        new_game_speed = current_game_speed
        points += 0.25
        if points % 100 == 0:
            new_game_speed = current_game_speed + 1

        text = font.render("Points: " + str(int(points)), True, (0, 0, 0))
        textRect = text.get_rect()
        textRect.center = (1000, 40)
        SCREEN.blit(text, textRect)

        return points, new_game_speed

    def background(current_game_speed):
        global x_pos_bg, y_pos_bg
        image_width = BG.get_width()
        SCREEN.blit(BG, (x_pos_bg, y_pos_bg))
        SCREEN.blit(BG, (image_width + x_pos_bg, y_pos_bg))
        if x_pos_bg <= -image_width:
            SCREEN.blit(BG, (image_width + x_pos_bg, y_pos_bg))
            x_pos_bg = 0
        x_pos_bg -= current_game_speed

    while run:
        for event in pygame.event.get():
            if event.type == pygame.QUIT:
                run = False
                exit()

        SCREEN.fill((255, 255, 255))

        num_dinos_alive = 0
        alter_game_speed = True

        for dino in dinos:
            if dino[0].is_alive == True:
                num_dinos_alive += 1
                if len(obstacles) != 0:
                    xy = obstacles[0].getXY()

                    input.distance_from_obstacle = xy[0]
                    input.obstacle_length = obstacles[0].getLength()
                    input.obstacle_height = obstacles[0].getHeight()
                    input.dino_height = dino[0].getXY()[1]
                    dino[0].points, game_speed = score(dino[0].points, input.game_speed)

                    if alter_game_speed == True:
                        input.game_speed = game_speed
                        alter_game_speed = False

                if GAME_MODE == "HUMAN_MODE":
                    userInput = playerKeySelector()
                else:
                    userInput = dino[1][0].forward(input.to_tensor())

                dino[0].update(userInput)
                dino[0].draw(SCREEN)

        if num_dinos_alive == 0:
            pygame.time.delay(1000)

            results = []
            for dino in dinos:
                results.append((dino[0].points, dino[1][1]))

            return results

        if len(obstacles) == 0 or obstacles[-1].getXY()[0] < spawn_dist:
            spawn_dist = random.randint(0, 670)
            if random.randint(0, 2) == 0:
                obstacles.append(SmallCactus(SMALL_CACTUS))
            elif random.randint(0, 2) == 1:
                obstacles.append(LargeCactus(LARGE_CACTUS))
            elif random.randint(0, 5) == 5:
                obstacles.append(Bird(BIRD))

        for obstacle in list(obstacles):
            obstacle.update(input.game_speed)
            obstacle.draw(SCREEN)

        background(input.game_speed)

        cloud.draw(SCREEN)
        cloud.update(input.game_speed)

        clock.tick(60)
        pygame.display.update()

        for obstacle in obstacles:
            for dino in dinos:
                if dino[0].dino_rect.colliderect(obstacle.rect):
                    dino[0].is_alive = False